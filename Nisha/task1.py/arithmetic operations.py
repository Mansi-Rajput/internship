# Integers
int_a = 5
int_b = 3

# Integer Arithmetic Operations
print(f"{int_a} + {int_b} = {int_a + int_b}")
print(f"{int_a} - {int_b} = {int_a - int_b}")
print(f"{int_a} * {int_b} = {int_a * int_b}")
print(f"{int_a} / {int_b} = {int_a / int_b}")
print(f"{int_a} % {int_b} = {int_a % int_b}")
print(f"{int_a} ** {int_b} = {int_a ** int_b}")

# Floats
float_a = 4.5
float_b = 2.5

# Float Arithmetic Operations
print(f"{float_a} + {float_b} = {float_a + float_b}")
print(f"{float_a} - {float_b} = {float_a - float_b}")
print(f"{float_a} * {float_b} = {float_a * float_b}")
print(f"{float_a} / {float_b} = {float_a / float_b}")

# Strings
str_a = "Hello"
str_b = "World"

# String Manipulations
print(f"Concatenation: {str_a + ' ' + str_b}")
print(f"Repetition: {str_a * 2}")
print(f"Slicing: {str_a[1:4]}")
print(f"Length of '{str_a}': {len(str_a)}")
print(f"Uppercase: {str_a.upper()}")
print(f"Lowercase: {str_b.lower()}")

# Booleans
print(f"5 == 5: {5 == 5}")
print(f"5 != 3: {5 != 3}")
print(f"6 > 2: {7 > 2}")
print(f"3 < 8: {3 < 8}")
print(f"4 >= 4: {4 >= 4}")
print(f"2 <= 5: {2 <= 5}")




