#Assigning values to variables 
a = 25
b  = 2.5
c =  "Hello"
d =  True

#Performing operations with variables
sum_ab = a + b
mul_ab = a * b
concatenated_cd = c + "World"
not_d = not d

print("Initial value and operations")
print(a)
print(b)
print(c)
print(d)
print()

print("Results of operations:")
print(sum_ab)
print(mul_ab)
print(concatenated_cd)
print(not_d)

#Variable reassignment
a = 10
b = "Intern"
c = False
print()
print("Resassigned values:")
print(a)
print(b)
print(c)

